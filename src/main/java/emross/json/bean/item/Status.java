package emross.json.bean.item;

public class Status {
	private float invite;
	private float ver;
	private float vs;
	private float vr;
	private float ce;
	private float he;

	public final float getInvite() {
		return invite;
	}

	public final void setInvite(float invite) {
		this.invite = invite;
	}

	public final float getVer() {
		return ver;
	}

	public final void setVer(float ver) {
		this.ver = ver;
	}

	public final float getVs() {
		return vs;
	}

	public final void setVs(float vs) {
		this.vs = vs;
	}

	public final float getVr() {
		return vr;
	}

	public final void setVr(float vr) {
		this.vr = vr;
	}

	public final float getCe() {
		return ce;
	}

	public final void setCe(float ce) {
		this.ce = ce;
	}

	public final float getHe() {
		return he;
	}

	public final void setHe(float he) {
		this.he = he;
	}

}